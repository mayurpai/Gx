package com.java.datatypes;

public class Primitive {

	public static void main(String[] args) {
		char characterPrimitiveDataType = 'A';
		byte bytePrimitiveDataType = 2;
		short shortPrimitiveDataType = 21;
		int intPrimitiveDataType = 5;
		float floatPrimitiveDataType = 5.123456f;
		double doublePrimitiveDataType = 4.12345678910d;

		System.out.println(characterPrimitiveDataType);
		System.out.println(bytePrimitiveDataType);
		System.out.println(shortPrimitiveDataType);
		System.out.println(intPrimitiveDataType);
		System.out.println(floatPrimitiveDataType);
		System.out.println(doublePrimitiveDataType);

	}

}
